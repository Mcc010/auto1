#Para definir 
# una función en Python se usa la palabra clave def, 
# seguida del nombre de la función, paréntesis () que pueden 
# contener parámetros, y finalmente dos puntos `: `. 
# El cuerpo de la función, es decir, el código a ejecutar, 
# debe estar indentado debajo de esa línea. Opcionalmente, 
# la función puede usar return para devolver un valor después de completar su tarea. 

#ejemplo 
def sumar(a, b):
    return a + b

# Llamando a la función
resultado = sumar(5, 3)
print("La suma es:", resultado)
